#include <stdio.h>

int main()
{
    // constants are fixed values that cannot be altered example speed of earths rotation 
    const float PI = 3.1459; // const keyword meand you cannot edit the value constant naming conventions recommend using upercase letters for naming constants
    printf("%.4f", PI);
    // when trying to change it accidentally error will be called
    return 0;
    
}
